<?php

/**
	Меняем пробелы простые на неразрывные в строках, похожих на телефонные номера
	+7 (495) 775-5795
*/

function smarty_modifier_phonize($text)
{
	// 	+7 (495) 775-5795
	$text = preg_replace('/(\+\d+)\s+(\(\d+\))\s+([\d]{2,3}\-[\d\-]{2,8})/', '$1 $2 $3', $text);
	// 	(4842) 77-40-80
	$text = preg_replace('/(\(\d+\))\s+([\d]{2,3}\-[\d\-]{2,8})/', '$1 $2', $text);
	return $text;
}
